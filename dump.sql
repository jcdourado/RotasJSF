CREATE DATABASE ROTAS;

USE ROTAS;

CREATE TABLE USUARIO(
	USUARIO VARCHAR(20) PRIMARY KEY, 
	NOME VARCHAR(50),
	EMAIL VARCHAR(50),
	SENHA VARCHAR(20)
);

CREATE TABLE ROTA(
	ID INT AUTO_INCREMENT PRIMARY KEY,
	NOME VARCHAR(50), 
	TEMPO INT
);

CREATE TABLE PONTO(
	ID INT AUTO_INCREMENT PRIMARY KEY,
	NUMERO VARCHAR(10),
	RUA VARCHAR(30), 
	CIDADE VARCHAR(30),
	ESTADO VARCHAR(20),
	IDROTA INT,
	FOREIGN KEY (IDROTA) REFERENCES ROTA(ID)
);

